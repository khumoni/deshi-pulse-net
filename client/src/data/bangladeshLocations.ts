export const getDivisions = () => {
  return Object.entries(bangladeshLocations.divisions).map(([key, division]) => ({
    key,
    name: division.name
  }));
};

export const getDistricts = (divisionKey: string) => {
  const division = bangladeshLocations.divisions[divisionKey];
  if (!division) return [];
  
  return Object.entries(division.districts).map(([key, district]) => ({
    key,
    name: district.name
  }));
};

export const getUpazilas = (divisionKey: string, districtKey: string) => {
  const division = bangladeshLocations.divisions[divisionKey];
  if (!division) return [];
  
  const district = division.districts[districtKey];
  if (!district) return [];
  
  return district.upazilas.map(upazila => ({
    key: upazila.toLowerCase().replace(/\s+/g, '-'),
    name: upazila
  }));
};